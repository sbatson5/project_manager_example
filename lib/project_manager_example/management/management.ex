defmodule ProjectManagerExample.Management do
  @moduledoc """
  The Management context.
  """

  import Ecto.Query, warn: false
  alias ProjectManagerExample.Repo

  alias ProjectManagerExample.Management.Project

  @doc """
  Returns the list of projects.

  ## Examples

      iex> list_projects()
      [%Project{}, ...]

  """
  def list_projects do
    Repo.all(Project)
  end

  @doc """
  Gets a single project.

  Raises `Ecto.NoResultsError` if the Project does not exist.

  ## Examples

      iex> get_project!(123)
      %Project{}

      iex> get_project!(456)
      ** (Ecto.NoResultsError)

  """
  def get_project!(id), do: Repo.get!(Project, id)

  @doc """
  Creates a project.

  ## Examples

      iex> create_project(%{field: value})
      {:ok, %Project{}}

      iex> create_project(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_project(attrs \\ %{}) do
    %Project{}
    |> Project.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a project.

  ## Examples

      iex> update_project(project, %{field: new_value})
      {:ok, %Project{}}

      iex> update_project(project, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_project(%Project{} = project, attrs) do
    project
    |> Project.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Project.

  ## Examples

      iex> delete_project(project)
      {:ok, %Project{}}

      iex> delete_project(project)
      {:error, %Ecto.Changeset{}}

  """
  def delete_project(%Project{} = project) do
    Repo.delete(project)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking project changes.

  ## Examples

      iex> change_project(project)
      %Ecto.Changeset{source: %Project{}}

  """
  def change_project(%Project{} = project) do
    Project.changeset(project, %{})
  end

  alias ProjectManagerExample.Management.Document

  @doc """
  Returns the list of documents.

  ## Examples

      iex> list_documents()
      [%Document{}, ...]

  """
  def list_documents(%{"status" => "published"}), do: list_documents_by_status(true)
  def list_documents(%{"status" => _}), do: list_documents_by_status(false)
  def list_documents(%{"project" => project_id}), do: list_documents_by_project(project_id)
  def list_documents(%{"views" => view_count}), do: list_documents_by_view_count(view_count)

  def list_documents(_) do
    Document
    |> Ecto.Query.preload([:project])
    |> Repo.all()
  end

  defp list_documents_by_status(status) do
    Document
    |> Ecto.Query.preload([:project])
    |> where(published: ^status)
    |> Repo.all()
  end

  defp list_documents_by_project(project_id) do
    Document
    |> Ecto.Query.preload([:project])
    |> where(project_id: ^project_id)
    |> Repo.all()
  end

  defp list_documents_by_view_count(view_count) do
    Document
    |> Ecto.Query.preload([:project])
    |> where([d], d.view_count >= ^view_count)
    |> Repo.all()
  end

