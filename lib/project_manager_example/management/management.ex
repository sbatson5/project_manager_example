  @doc """
  Gets a single user.

  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(identifier), do: user_by_id_or_screen_name(identifier)

  defp user_by_id_or_screen_name(identifier) when is_integer(identifier) do
    Repo.get!(User, identifier)
  end

  defp user_by_id_or_screen_name(identifier) when is_binary(identifier) do
    case Integer.parse(identifier) do
      {int, ""} ->
        user_by_id_or_screen_name(int)
      _error ->
        User
        |> where(screen_name: ^identifier)
        |> Repo.one!()
    end
  end

  def create_user(attrs \\ %{}) do
    %User{}
    |> User.changeset(attrs)
    |> Repo.insert()
  end

